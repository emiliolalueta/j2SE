package customtags;

/**
 *
 * @author Alumno
 */
public class Club {
  private String nombre;
    private String fundacion;
    private String presidente;
    private String presupuesto;
    private String equipacion;

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the fundacion
     */
    public String getFundacion() {
        return fundacion;
    }

    /**
     * @param fundacion the fundacion to set
     */
    public void setFundacion(String fundacion) {
        this.fundacion = fundacion;
    }

    /**
     * @return the presidente
     */
    public String getPresidente() {
        return presidente;
    }

    /**
     * @param presidente the presidente to set
     */
    public void setPresidente(String presidente) {
        this.presidente = presidente;
    }

    /**
     * @return the presupuesto
     */
    public String getPresupuesto() {
        return presupuesto;
    }

    /**
     * @param presupuesto the presupuesto to set
     */
    public void setPresupuesto(String presupuesto) {
        this.presupuesto = presupuesto;
    }

    /**
     * @return the equipacion
     */
    public String getEquipacion() {
        return equipacion;
    }

    /**
     * @param equipacion the equipacion to set
     */
    public void setEquipacion(String equipacion) {
        this.equipacion = equipacion;
    }
}
